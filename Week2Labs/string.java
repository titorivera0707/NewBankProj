
public class string {
    public static void main(String[] args) {

        String firstString = "The red fox jumps over the sleepy sheep";

        //Gets a certain character at a certain position
        System.out.println(firstString.charAt(8));
        //Sees whether there is any information in a certain string.
        System.out.println(firstString.isEmpty());
        //Gets the length of the String
        System.out.println(firstString.length());
        //Converts the entire string to lowercase
        System.out.println(firstString.toLowerCase());
        //Converts the entire string to uppercase
        System.out.println(firstString.toUpperCase());

    }
}